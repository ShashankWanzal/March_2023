package polymorphism;

public class Demo1 
{

	
	
	public void m1()
	{
		
		System.out.println("M1 is Running");
	}
	
	public void m1(int a)
	{
		
		System.out.println("M1 is Running=> "+a);
	}
	
	public void m1(int a,int b)
	{
		
		System.out.println("M1 is Running=> "+a+b);
	}
	
	public void m1(int a , int b, int c)
	{
		
		System.out.println("M1 is Running=> "+(a+b+c));
	}
	
	public void m1(int a, int b,int c,int d)
	{
		
		System.out.println("M1 is Running=> "+(a+b+c+d));
	}
	

	

	
	
	
}
