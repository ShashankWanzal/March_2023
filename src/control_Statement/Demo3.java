package control_Statement;

public class Demo3
{

	//else if 
	
	public static void main(String[] args)
	{
	
		int mark=34;
		
		
		
		if(mark>=65)
		{
			
			System.out.println("Distinction");
		}
		
		else if(mark>=60)
		{
			System.out.println("First class");
		}
		
		else if(mark>=50)
		{
			System.out.println("Second Class");
		}
		
		else if(mark>=35)
		{
			System.out.println("Pass");
		}
		
		
		else 
		{
			System.out.println("fail");
		}
		
		
		
	}
	
	
	
	
}
